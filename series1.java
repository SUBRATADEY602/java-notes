import java.util.*;

public class series1 {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int i, n;
        System.out.println(" enter the value of n");
        n = sc.nextInt();
        for (i = 1; i <= n; i++) {
            System.out.println(2 * i);
        }
    }
}
